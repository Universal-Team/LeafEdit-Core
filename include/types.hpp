#ifndef _LEAFEDIT_CORE_TYPES_HPP
#define _LEAFEDIT_CORE_TYPES_HPP

#include <3ds.h>
#include <stdint.h>

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

typedef int8_t s8;
typedef int16_t s16;
typedef int32_t s32;
typedef int64_t s64;

/*
	SaveTypes.
*/
enum class SaveType : u8 {
	WW,
	NL,
	WA,
	HHD,
	UNUSED
};

/*
	Save Regions. (Mainly for AC:WW)
*/
enum class WWRegion : u8 {
	EUR_USA, // EUR + USA are the same.
	JPN,
	KOR
};

#endif